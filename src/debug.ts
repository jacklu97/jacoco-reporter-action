async function run() {
  // const xmlPath = core.getInput(ACTION_INPUTS.JACOCO_XML_PATH);
  // const token = core.getInput(ACTION_INPUTS.GITHUB_TOKEN);

  // const octokit = getOctokit(token);

  let commentContent: string | undefined = undefined;

  const xmlPath = "target/site/jacoco/jacoco.xml"

  const xml: string | null = safeRun(() => readFileSync(xmlPath, "utf-8"));

  if (!xml) {
    commentContent = generateNoXmlComment();
  } else {
    const processedMetricData: ProccessedMetric[] = METRIC_FIELDS.map(metric => {
      return {
        data: metric,
        extractedData: extract(xml, metric.xmlField)
      }
    })

    // console.log(processedMetricData)

    publishActionOutput(processedMetricData)

    const baseline = null;

    commentContent = generateResultComment({
      metrics: processedMetricData,
      ctx: context,
      baseline,
    });
  }

  await publishComment(commentContent);
}

run().catch(core.setFailed);
